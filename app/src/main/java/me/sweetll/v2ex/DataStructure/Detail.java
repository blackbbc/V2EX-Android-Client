public class Detail {
    public String userName = "";
    public String time = "";
    public String content = "";
    public String floor = "";
    public String imageSrc = "";

    public Detail(String userName, String time, String content, String floor, String imageSrc) {
        this.userName = userName;
        this.time = time;
        this.content = content;
        this.floor = floor;
        this.imageSrc = imageSrc;
    }

}
